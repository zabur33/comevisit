<script>
document.addEventListener('DOMContentLoaded', function() {
  function toggleContent(idPrefix) {
    const benefitsSummary = document.getElementById(`benefits-summary-${idPrefix}`);
    const moreBenefits = document.getElementById(`more-benefits-${idPrefix}`);
    const btn = document.getElementById(`read-more-btn-${idPrefix}`);

    if (!benefitsSummary || !moreBenefits || !btn) {
      console.error('Element not found for ID prefix:', idPrefix);
      return;
    }

    if (moreBenefits.style.display === 'none' || moreBenefits.style.display === '') {
      moreBenefits.style.display = 'block';
      btn.textContent = 'Read Less';
      // Ensure only 'more-benefits' section is toggled, keep 'benefits-summary' visible
      benefitsSummary.style.display = 'block';
    } else {
      moreBenefits.style.display = 'none';
      btn.textContent = 'Read More';
      // Ensure 'benefits-summary' is visible when 'more-benefits' is hidden
      benefitsSummary.style.display = 'block';
    }
  }

  document.getElementById('read-more-btn-kunyit').addEventListener('click', function(event) {
    event.preventDefault();
    toggleContent('kunyit');
  });

  document.getElementById('read-more-btn-beras-kencur').addEventListener('click', function(event) {
    event.preventDefault();
    toggleContent('beras-kencur');
  });

  document.getElementById('read-more-btn-wedang-jahe').addEventListener('click', function(event) {
    event.preventDefault();
    toggleContent('wedang-jahe');
  });
});
</script>

